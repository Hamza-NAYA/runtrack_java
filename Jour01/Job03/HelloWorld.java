package Jour01.Job03; // Déclaration du package, assure que la structure des dossiers correspond à celle du package

// Déclaration de la classe publique HelloWorld
public class HelloWorld {

    // La méthode principale main, point d'entrée de l'application Java
    public static void main(String[] args) {
        // Affiche "Hello world" dans le terminal
        System.out.println("Hello world");
    }
    
    /*
     * La méthode main est statique car elle doit être appelée
     * sans avoir besoin d'instancier la classe HelloWorld.
     * Elle accepte un tableau de chaînes de caractères en argument
     * qui peut être utilisé pour passer des paramètres en ligne de commande.
     */
}
